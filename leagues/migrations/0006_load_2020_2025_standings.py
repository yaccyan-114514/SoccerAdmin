def load_standings(apps, schema_editor):
    Team = apps.get_model("teams", "Team")
    TeamStanding = apps.get_model("leagues", "TeamStanding")
    team_cache = {t.short_name: t for t in Team.objects.all()}

    for year, rows in SEASON_DATA.items():
        for row in rows:
            (
                short_name, matches, wins, draws, losses,
                goals_for, goals_against, points,
                assists, yellow_cards, red_cards, penalties,
                shots, shots_on_target, offsides, corners,
                passes, fouls, saves,
            ) = row
            team = team_cache.get(short_name)
            if not team:
                continue
            TeamStanding.objects.update_or_create(
                team=team,
                year=year,
                defaults={
                    "matches_played": matches,
                    "wins": wins,
                    "draws": draws,
                    "losses": losses,
                    "goals_for": goals_for,
                    "goals_against": goals_against,
                    "points": points,
                    "assists": assists,
                    "yellow_cards": yellow_cards,
                    "red_cards": red_cards,
                    "penalties": penalties,
                    "shots": shots,
                    "shots_on_target": shots_on_target,
                    "offsides": offsides,
                    "corners": corners,
                    "passes": passes,
                    "fouls": fouls,
                    "saves": saves,
                },
            )


def clear_standings(apps, schema_editor):
    TeamStanding = apps.get_model("leagues", "TeamStanding")
    TeamStanding.objects.filter(year__in=[2020, 2021, 2022, 2023, 2024, 2025]).delete()


class Migration(migrations.Migration):

    dependencies = [
        ("leagues", "0005_load_2026_team_stats"),
    ]

    operations = [
        migrations.RunPython(load_standings, clear_standings),
    ]
